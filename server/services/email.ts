import nodemailer from 'nodemailer';

// Create email transporter
function createTransporter() {
  return nodemailer.createTransport({
    service: 'gmail',
    auth: {
      user: process.env.EMAIL_USER, // Your Gmail address
      pass: process.env.EMAIL_PASS  // Your Gmail app password
    }
  });
}

export interface EmailOptions {
  to: string;
  subject: string;
  html: string;
  from?: string;
}

export async function sendEmail(options: EmailOptions): Promise<boolean> {
  try {
    // Use environment variables or fallback to demo mode
    if (!process.env.EMAIL_USER || !process.env.EMAIL_PASS) {
      console.log('üìß EMAIL DEMO MODE - Would send email:');
      console.log(`To: ${options.to}`);
      console.log(`Subject: ${options.subject}`);
      console.log(`From: ${options.from || 'noreply@venuine.com'}`);
      console.log('---');
      return true; // Return success for demo
    }

    const transporter = createTransporter();
    
    const mailOptions = {
      from: options.from || process.env.EMAIL_USER,
      to: options.to,
      subject: options.subject,
      html: options.html
    };

    const result = await transporter.sendMail(mailOptions);
    console.log('‚úÖ Email sent successfully:', result.messageId);
    return true;
    
  } catch (error) {
    console.error('‚ùå Email sending failed:', error);
    return false;
  }
}

